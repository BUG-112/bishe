var mongoose = require('mongoose')

var Schema = mongoose.Schema

var adminSchema = new Schema({
    phone: {
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    created_time: {
        type: Date,
        default: Date.now
    },
    last_modified_time: {
        type: Date,
        default: Date.now
    }
})
module.exports = mongoose.model('Admin', adminSchema)